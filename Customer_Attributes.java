package customer_details;

public class Customer_Attributes {
	private String company;
	private int customer_id;
	private String customer_name;
	private String address;
	private String customer_GSTIN;
	private String customer_emailid;
	private String customer_contactnumber;
	private String customer_city;
	private String State;
	private String pincode;
	
	
	public int getCustomer_id() {
		return customer_id;
	}
	public void setCustomer_id(int customer_id) {
		this.customer_id = customer_id;
	}
	public String getCustomer_name() {
		return customer_name;
	}
	public void setCustomer_name(String customer_name) {
		this.customer_name = customer_name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getCustomer_GSTIN() {
		return customer_GSTIN;
	}
	public void setCustomer_GSTIN(String customer_GSTIN) {
		this.customer_GSTIN = customer_GSTIN;
	}
	public String getCustomer_emailid() {
		return customer_emailid;
	}
	public void setCustomer_emailid(String customer_emailid) {
		this.customer_emailid = customer_emailid;
	}
	public String getCustomer_contactnumber() {
		return customer_contactnumber;
	}
	public void setCustomer_contactnumber(String contactnumber) {
		this.customer_contactnumber = contactnumber;
	}
	public String getCustomer_city() {
		return customer_city;
	}
	public void setCustomer_city(String customer_city) {
		this.customer_city = customer_city;
	}
	public String getState() {
		return State;
	}
	public void setState(String state) {
		State = state;
	}
	public String getPincode() {
		return pincode;
	}
	public void setPincode(String pincode) {
		this.pincode = pincode;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	
	

}
 